// CLI, include only if not testing

void error_usage() {
    fprintf(stderr, "Usage:   run <checkpoint> [options]\n");
    fprintf(stderr, "Example: run model.bin -n 256 -i \"Once upon a time\"\n");
    fprintf(stderr, "Options:\n");
    fprintf(stderr, "  -t <float>  temperature in [0,inf], default 1.0\n");
    fprintf(stderr, "  -p <float>  p value in top-p (nucleus) sampling in [0,1] default 0.9\n");
    fprintf(stderr, "  -s <int>    random seed, default time(NULL)\n");
    fprintf(stderr, "  -n <int>    number of steps to run for, default 256. 0 = max_seq_len\n");
    fprintf(stderr, "  -i <string> input prompt\n");
    fprintf(stderr, "  -z <string> optional path to custom tokenizer\n");
    fprintf(stderr, "  -m <string> mode: generate|chat, default: generate\n");
    fprintf(stderr, "  -y <string> (optional) system prompt in chat mode\n");
    exit(EXIT_FAILURE);
}

int main(int argc, char *argv[]) {

    // default parameters
    char *checkpoint_path = NULL;  // e.g. out/model.bin
    // poor man's C argparse so we can override the defaults above from the command line
    if (argc >= 2) { checkpoint_path = argv[1]; } else { error_usage(); }

    // build the Transformer via the model .bin file
    Transformer transformer;
    build_transformer(&transformer, checkpoint_path);

    float *wcls;
    size_t vocab_size = transformer.config.vocab_size;
    size_t dim = transformer.config.dim;
    size_t nbyte = vocab_size * dim * sizeof(float);
    wcls = (float*) malloc(nbyte);
    memcpy(wcls, transformer.weights.token_embedding_table, nbyte);
    transpose_weight(wcls, vocab_size, dim);
    update_weight(&transformer);

    free_transformer(&transformer);

    FILE *file;
    file = fopen(checkpoint_path, "a");
    if (file == NULL) {
        perror("fopen");
        exit(1);
    }
    fseek(file, 0, SEEK_END);
    size_t ret = fwrite(wcls, sizeof(float), vocab_size * dim, file);
    printf("write %lu / %lu in float.", ret, vocab_size * dim);
    fclose(file);

    return 0;
}
